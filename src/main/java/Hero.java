public class Hero {
    private int x;
    private int y;

    public Hero(int xx, int yy){
        x = xx;
        y = yy;
    }
    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public void setX(int newx) {
        x = newx;
    }

    public void setY(int newy){
        y = newy;
    }
}
